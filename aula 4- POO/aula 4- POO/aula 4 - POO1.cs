public class Metodos
{
    public double Hora(double x)
    {
        double hora = x * 60;
        return hora;
    }

    public double Minutos(double x)
    {
        double minutos = x * 60;
        return minutos;
    }

    public double Segundos(double x)
    {
        double segundos = x * 3600;
        return segundos;

    }

}

